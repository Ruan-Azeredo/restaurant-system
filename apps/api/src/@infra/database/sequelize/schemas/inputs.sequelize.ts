import { DataTypes } from "sequelize";
import sequelize from "../connection";

const InputModel = sequelize.define("inputs", {
  id: {
    type: DataTypes.UUID,
    defaultValue: DataTypes.UUIDV4,
    primaryKey: true,
  },
  name: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  stock_quantity: {
    type: DataTypes.FLOAT,
    allowNull: false,
  },
  quantity_unit: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  createdAt: {
    type: DataTypes.DATE,
    defaultValue: DataTypes.NOW,
  },
  updatedAt: {
    type: DataTypes.DATE,
    defaultValue: DataTypes.NOW,
  },
});

export default InputModel;
